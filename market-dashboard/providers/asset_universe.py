def alias_rows() -> list[dict[str, str]]:
    rows: list[dict[str, str]] = []
    for symbol, name in CN_INDEX_NAMES.items():
        rows.append({"source_symbol": symbol, "alias": name, "market": "cn", "asset_key": f"YFIDX:{symbol}", "alias_type": "index"})
    for symbol, (name, aliases) in CN_EQUITIES.items():
        asset_key = f"YFCN:{symbol}"
        for alias in [symbol, name, *aliases]:
            rows.append({"source_symbol": symbol, "alias": alias, "market": "cn", "asset_key": asset_key, "alias_type": "equity"})
    for symbol, (name, aliases) in US_ASSETS.items():
        asset_key = f"YFUS:{symbol}"
        for alias in [symbol, name, *aliases]:
            rows.append({"source_symbol": symbol, "alias": alias, "market": "us", "asset_key": asset_key, "alias_type": "equity_or_etf"})
    return rows
